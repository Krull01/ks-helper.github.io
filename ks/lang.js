// Сортируем словарь по длине фрагмента (чтобы длинные фразы переводились раньше коротких во избежание багов)
translations.sort((a, b) => b.ru.length - a.ru.length);

let currentLang = 'ua'; // Временно ставим UA, чтобы при загрузке скрипт перевел вшитые украинские слова в RU

function changeLanguage(targetLang) {
    if (currentLang === targetLang) return;
    
    const fromLang = targetLang === 'ru' ? 'ua' : 'ru';
    const toLang = targetLang;
    
    function replaceText(node) {
        if (node.nodeType === Node.TEXT_NODE) {
            let text = node.nodeValue;
            let changed = false;
            
            translations.forEach(dict => {
                if (text.includes(dict[fromLang])) {
                    text = text.split(dict[fromLang]).join(dict[toLang]);
                    changed = true;
                }
            });
            if (changed) node.nodeValue = text;
        } else {
            if (node.placeholder) {
                let text = node.placeholder;
                translations.forEach(dict => {
                    if (text.includes(dict[fromLang])) {
                        text = text.split(dict[fromLang]).join(dict[toLang]);
                    }
                });
                node.placeholder = text;
            }
            node.childNodes.forEach(child => replaceText(child));
        }
    }
    
    replaceText(document.body);
    currentLang = targetLang;
}

document.addEventListener('DOMContentLoaded', () => {
    const langBtns = document.querySelectorAll('.lang-btn');
    
    // При старте выравниваем весь смешанный текст под дефолтный RU язык
    // Так как у нас в HTML язык смешанный, пройдемся с переключением UA -> RU
    changeLanguage('ru');

    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            langBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            
            const selectedLang = btn.getAttribute('data-lang');
            changeLanguage(selectedLang);
        });
    });
});
